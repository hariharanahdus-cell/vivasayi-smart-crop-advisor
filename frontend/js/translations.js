window.i18n = {
  getLang: () => localStorage.getItem('sca_lang') || 'en',
  setLang: (lang) => {
    localStorage.setItem('sca_lang', lang);
    location.reload();
  },
  t: (key) => translations[window.i18n.getLang()][key] || key,
  apply: () => {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (el.tagName === 'INPUT' && el.type === 'placeholder') {
         // Handle input placeholders later if needed
      } else {
         el.textContent = window.i18n.t(key);
      }
    });

    // Special: select options
    document.querySelectorAll('option[data-i18n]').forEach(el => {
       el.textContent = window.i18n.t(el.getAttribute('data-i18n'));
    });

    // Translate toggle button specifically
    const btn = document.getElementById('langToggleBtn');
    if (btn) btn.textContent = window.i18n.t('lang_toggle');
  }
};

document.addEventListener('DOMContentLoaded', window.i18n.apply);
